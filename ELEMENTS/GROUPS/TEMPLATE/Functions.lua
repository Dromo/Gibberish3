-------------------------------------------------------------------------------------
--      Description:    constructor array workaround
-------------------------------------------------------------------------------------
--        Parameter:    index
--                      data
-------------------------------------------------------------------------------------
--           Return:    group template element
-------------------------------------------------------------------------------------
Group.Constructor[Group.Types.TEMPLATE] = function (index, data)

    return TEMPLATE.TemplateElement(index, data)

end



-------------------------------------------------------------------------------------
--      Description:    returns a list of all allowed timers
-------------------------------------------------------------------------------------
--        Parameter:    
-------------------------------------------------------------------------------------
--           Return:    list of all allowed timers for group listbox
-------------------------------------------------------------------------------------
Group.GetAllowedTimer[Group.Types.TEMPLATE] = function ()

    return {    Timer.Types.BAR,
                Timer.Types.ICON }

end
