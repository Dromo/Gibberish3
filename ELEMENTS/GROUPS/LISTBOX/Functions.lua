-------------------------------------------------------------------------------------
--      Description:    constructor array workaround
-------------------------------------------------------------------------------------
--        Parameter:    index
--                      data
-------------------------------------------------------------------------------------
--           Return:    group listbox element
-------------------------------------------------------------------------------------
Group.Constructor[Group.Types.LISTBOX] = function (index, data)

    return LISTBOX.ListBoxElement(index, data)

end



-------------------------------------------------------------------------------------
--      Description:    returns a list of all allowed timers
-------------------------------------------------------------------------------------
--        Parameter:    
-------------------------------------------------------------------------------------
--           Return:    list of all allowed timers for group listbox
-------------------------------------------------------------------------------------
Group.GetAllowedTimer[Group.Types.LISTBOX] = function ()

    return {    Timer.Types.BAR,
                Timer.Types.ICON }

end
