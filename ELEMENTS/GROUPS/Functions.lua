-------------------------------------------------------------------------------------
--      Description:    selection changed event
-------------------------------------------------------------------------------------
--        Parameter:    
-------------------------------------------------------------------------------------
--           Return:    
-------------------------------------------------------------------------------------
function Group.SelectionChanged(index)

    Data.selectedGroupIndex = index

    for key, group in ipairs(Group) do

        group:SelectionChanged()

    end

end