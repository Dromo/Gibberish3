--===================================================================================
--             Name:    click block
-------------------------------------------------------------------------------------
--      Description:    blocks the next click for a child
--===================================================================================
Options.Constructor.ClickBlock = class(Turbine.UI.Window)






-------------------------------------------------------------------------------------
--      Description:    click block constructor
-------------------------------------------------------------------------------------
--        Parameter:    parent
-------------------------------------------------------------------------------------
--           Return:    group listbox element
-------------------------------------------------------------------------------------
function Options.Constructor.ClickBlock:Constructor( parent )
	Turbine.UI.Window.Constructor( self )

    self:SetParent(         parent )
    self:SetVisible(        false )
    self:SetMouseVisible(   true )
    self:SetZOrder(         50 )

    self.child            = nil

end


-------------------------------------------------------------------------------------
--      Description:    mouse click
-------------------------------------------------------------------------------------
--        Parameter:    
-------------------------------------------------------------------------------------
--           Return:   
-------------------------------------------------------------------------------------
function Options.Constructor.ClickBlock:MouseClick( sender, args )

    if self.child ~= nil then
    
        self.child:Hide()
        self:Hide()

    end

end


-------------------------------------------------------------------------------------
--      Description:    show
-------------------------------------------------------------------------------------
--        Parameter:    child
-------------------------------------------------------------------------------------
--           Return:   
-------------------------------------------------------------------------------------
function Options.Constructor.ClickBlock:Show( child )

    self.child = child
    self:SetVisible(true)

end


-------------------------------------------------------------------------------------
--      Description:    hide
-------------------------------------------------------------------------------------
--        Parameter:    
-------------------------------------------------------------------------------------
--           Return:   
-------------------------------------------------------------------------------------
function Options.Constructor.ClickBlock:Hide()

    self.child = nil
    self:SetVisible(false)

end

