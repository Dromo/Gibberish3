-------------------------------------------------------------------------------------
--        Parameter:    
-------------------------------------------------------------------------------------
--           Return:    
-------------------------------------------------------------------------------------
function Options.MainWindow.OpenClose()

    if Options.MainWindow.Window == nil then
    
        Options.MainWindow.Window = Options.Constructor.OptionsWindow()

    else

        Options.MainWindow.Window:Finish()

        Options.MainWindow.Window = nil

    end
    
end